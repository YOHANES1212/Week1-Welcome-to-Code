let num1 = 0;
while (num1 < 10) {
  num1++;
  console.log(`LOOPING PERTAMA ${num1}`);
}

let num2 = 10;
while (num2 > 0) {
  num2--;
  console.log(`LOOPING KEDUA ${num2}`);
}
